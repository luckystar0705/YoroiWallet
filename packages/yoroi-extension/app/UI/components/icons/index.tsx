interface IconWrapperProps {
  icon: Icons;
  iconProps?: React.SVGProps<SVGSVGElement>;
  asButton?: boolean;
  iconButtonId?: string;
  [key: string]: any;
}

export function IconWrapper({
  icon,
  iconProps = {},
  asButton = false,
  buttonProps = {},
  iconButtonId = '',
  ...props
}: Readonly<IconWrapperProps>): React.ReactNode {
  const IconComponent = typeof icon === 'string' ? Icon[icon] : icon;
  if (IconComponent == null) {
    throw new Error('A proper icon component or reference was expected but got: ' + icon);
  }
  if (asButton) {
    return (
      <IconButton {...buttonProps} id={props.iconButtonId ?? 'icon-button'}>
        <Box component="span" display="inline-flex" color="ds.el_gray_medium" {...props}>
          <IconComponent {...iconProps} />
        </Box>
      </IconButton>
    );
  }

  return (
    <Box color="ds.el_gray_medium" display="inline-flex" {...props}>
      <IconComponent {...iconProps} />
    </Box>
  );
}
